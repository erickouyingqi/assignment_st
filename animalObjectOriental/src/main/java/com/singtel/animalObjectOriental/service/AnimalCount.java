package com.singtel.animalObjectOriental.service;

public class AnimalCount {
    private int flyCount;
    private int walkCount;
    private int singCount;
    private int swimCount;

    public int getFlyCount() {
        return flyCount;
    }

    public void setFlyCount(int flyCount) {
        this.flyCount = flyCount;
    }

    public int getWalkCount() {
        return walkCount;
    }

    public void setWalkCount(int walkCount) {
        this.walkCount = walkCount;
    }

    public int getSingCount() {
        return singCount;
    }

    public void setSingCount(int singCount) {
        this.singCount = singCount;
    }

    public int getSwimCount() {
        return swimCount;
    }

    public void setSwimCount(int swimCount) {
        this.swimCount = swimCount;
    }
}
